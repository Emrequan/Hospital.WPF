-- Oracle SQL Developer ile çalıştırılacak script
-- Hastane Entegrasyon Sistemi - Veritabanı Oluşturma Scripti

-- 1. VAKIF Hastanesi için tablo oluşturma
CREATE TABLE VAKIF_HASTALAR (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    AD VARCHAR2(50) NOT NULL,
    SOYAD VARCHAR2(50) NOT NULL,
    DOGUM_TARIHI DATE NOT NULL,
    KAYIT_TARIHI DATE DEFAULT SYSDATE
);

-- 2. DEVLET Hastanesi için tablo oluşturma  
CREATE TABLE DEVLET_HASTALAR (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ISIM VARCHAR2(50) NOT NULL,
    SOYISIM VARCHAR2(50) NOT NULL,
    DOGUMYILI VARCHAR2(10) NOT NULL,
    KAYIT_TARIHI DATE DEFAULT SYSDATE
);

-- 3. MASTER (Merkezi) veritabanı için tablo oluşturma
CREATE TABLE MASTER_HASTALAR (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    AD VARCHAR2(50) NOT NULL,
    SOYAD VARCHAR2(50) NOT NULL,
    DOGUM_TARIHI DATE NOT NULL,
    HASTANE_NO NUMBER NOT NULL,
    KAYIT_TARIHI DATE DEFAULT SYSDATE,
    KAYNAK_HASTANE VARCHAR2(50)
);

